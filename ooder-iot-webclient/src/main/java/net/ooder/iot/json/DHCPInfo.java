package  net.ooder.iot.json;

public class DHCPInfo {
   String name;
   String mac;
   String ip;

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public String getMac() {
		return mac;
	}

	public void setMac(String mac) {
		this.mac = mac;
	}

	public String getName() {
		return name;
	}
public void setName(String name) {
	this.name = name;
}
   
}
